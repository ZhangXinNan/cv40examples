# 诉求：计算边长为3的正方形的面积


# 构造函数area1，无参数、无返回值
def area1():
    print("1:无参数、无返回值：")
    print("边长为3的正方形面积：",3*3)
# 构造函数area2，无参数、有返回值
def area2():
    print("2:无参数、有返回值：")
    return 3*3   #返回值
# 构造函数area3，有参数、无返回值
def area3(r):
    print("3:有参数、无返回值：")
    print("边长为",r,"的正方形面积为:",r*r)
# 构造函数area4,有参数，有返回值
def area4(r):
    print("4:有参数，有返回值：")
    return  r*r
# 调用函数
area1()
print("边长为3的正方形面积为：",area2())
area3(3)
print("边长为3的正方形面积为:",area4(3))
# 不用函数直接计算
area=3*3
print("5:直接计算边长3的长方形面积：",area)








