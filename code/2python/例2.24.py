# 如果有返回值，函数的运行结果，作为一个值来使用
# ========参数总结========
# 无参数，函数的功能是固定的，比如，计算半径为10的面积，只能完成该运算
# 有参数，函数的功能更加灵活，比如，它能够计算不同半径的圆面积


